package de.rafael.plugins.creeper.recover;

import de.rafael.plugins.creeper.recover.common.CreeperPlugin;
import de.rafael.plugins.creeper.recover.scheduler.FoliaScheduler;
import org.bukkit.Bukkit;

import java.util.concurrent.TimeUnit;

/**
 * @author Rafael K.
 * @since 06/07/2023
 */

public class CreeperRecover extends CreeperPlugin {

    @Override
    public void onLoad() {
        super.onLoad();

        this.scheduler = new FoliaScheduler();
    }

    @Override
    public void onDisable() {
        super.onDisable();
    }

    @Override
    public void onEnable() {
        super.onEnable();

        // Tasks
        Bukkit.getAsyncScheduler().runAtFixedRate(this, scheduledTask -> this.pluginStats.tick(), 0, 5, TimeUnit.MINUTES);
    }

}
